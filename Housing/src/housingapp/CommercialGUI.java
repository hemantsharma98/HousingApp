    private void deletebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deletebtnActionPerformed
        // TODO add your handling code here:
        owner = nmtf.getText();
        county =ctcb.getSelectedItem().toString();
        type = ptcb.getSelectedItem().toString();
        Commercial c;
        cCount = cList.size();

        for (int i = 0; i < cCount; i++) {
            c = cList.get(i);
            if (c.getOwner().equalsIgnoreCase(owner) && c.getCounty().equalsIgnoreCase(county)) {
                cList.remove(i);
                cCount = cCount - 1;

                JOptionPane.showMessageDialog(null, "Details have been removed");
                   writeToFile();
            }
        }
        nmtf.setText("");
        ptcb.setSelectedIndex(0);
        ctcb.setSelectedIndex(0);
        mntf.setText("");
        mxtf.setText("");
        fscb.setSelectedIndex(0);
    }//GEN-LAST:event_deletebtnActionPerformed

