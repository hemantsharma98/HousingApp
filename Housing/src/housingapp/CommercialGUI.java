    private void addbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addbtnActionPerformed
        // TODO add your handling code here:
        h=new Commercial();
        h.setOwner(nmtf.getText());
        h.setCounty(ctcb.getSelectedItem().toString());
        h.setType(ptcb.getSelectedItem().toString());
        ((Commercial) h).setMinm(Double.parseDouble(mntf.getText()));
        ((Commercial) h).setMaxm(Double.parseDouble(mxtf.getText()));
        ((Commercial) h).setFsale(fscb.getSelectedItem().toString());

        cList.add((Commercial) h);

        JOptionPane.showMessageDialog(null, "Details have been added to Commercial");

        nmtf.setText("");
        ptcb.setSelectedIndex(0);
        ctcb.setSelectedIndex(0);
        mntf.setText("");
        mxtf.setText("");
        fscb.setSelectedIndex(0);

        writeToFile();

    }//GEN-LAST:event_addbtnActionPerformed

